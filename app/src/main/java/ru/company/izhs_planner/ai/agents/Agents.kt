class CodesCheckerAgent {
    fun generate(context: String): String = run {
        val checks = listOf(
            CodeCheck("Отступ от границы участка", true, "Рекомендуется не менее 3 метров до дома"),
            CodeCheck("Отступ от соседнего дома", true, "Не менее 6 метров между домами"),
            CodeCheck("Этажность", true, "Максимум 3 этажа для ИЖС"),
            CodeCheck("Высота этажа", true, "Рекомендуется 2.7-3.0 м"),
            CodeCheck("Площадь застройки", true, "Не более 50% от участка"),
            CodeCheck("Инсоляция", true, "Не менее 2 часов в день для жилых комнат"),
            CodeCheck("Уклон участка", null, "При >10% — террасирование или свайный фундамент")
        )

        val results = checks.joinToString("\n") { check ->
            when (check.status) {
                true -> "✅ ${check.name}: ${check.note}"
                false -> "❌ ${check.name}: ${check.note}"
                null -> "⚠️ ${check.name}: ${check.note}"
            }
        }

        """
            Проверка на соответствие нормам СП 11-101-95, СП 55.13330, СП 42.13330:
            
            $results
            
            ---
            **Заключение: Требуется проверка архитектором**
            
            Для получения разрешения на строительство необходимо:
            1. Архитектурный проект
            2. Согласования в администрации
            3. Экспертиза (при необходимости)
            
            Внимание: Данная проверка носит справочный характер.
        """.trimIndent()
    }

    private data class CodeCheck(
        val name: String,
        val status: Boolean?,
        val note: String
    )
}